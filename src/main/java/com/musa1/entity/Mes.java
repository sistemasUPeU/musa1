package com.musa1.entity;

public class Mes {
	int id_mes;
	String mes;
	public Mes(int id_mes, String mes) {
		super();
		this.id_mes = id_mes;
		this.mes = mes;
	}
	public int getId_mes() {
		return id_mes;
	}
	public void setId_mes(int id_mes) {
		this.id_mes = id_mes;
	}
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	} 
	
	
	
}
